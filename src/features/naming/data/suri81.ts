/** 수리 길흉 판정. 81 초과 시 80을 빼서 환원한다. */
export function getSuriInfo(value: number): SuriInfo {
    const normalized = value > 81 ? ((value - 1) % 80) + 1 : value;
    const found = SURI_TABLE[normalized];
    if (found) return found;
    return {
        grade: '흉',
        title: BAD_SURI_TITLES[normalized] || `흉수(${normalized})`,
    };
}

/** 길수 여부 (대길/길만 인정, 평/흉 제외) */
export function isGoodSuri(value: number): boolean {
    const grade = getSuriInfo(value).grade;
    return grade === '대길' || grade === '길';
}
